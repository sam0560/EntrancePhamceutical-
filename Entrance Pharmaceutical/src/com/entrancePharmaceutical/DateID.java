package com.entrancePharmaceutical;

public class DateID {
    short orderMonth;
    short orderDay;
    short orderYear;

    public short getOrderMonth(short orderMonth) {
        return orderMonth;
    }

    public void setOrderMonth(short orderMonth) {
        this.orderMonth = orderMonth;
    }

    public short getOrderDay(short orderDay) {
        return orderDay;
    }

    public void setOrderDay(short orderDay) {
        this.orderDay = orderDay;
    }

    public short getOrderYear(short orderYear) {
        return orderYear;
    }

    public void setOrderYear(short orderYear) {
        this.orderYear = orderYear;
    }
}
